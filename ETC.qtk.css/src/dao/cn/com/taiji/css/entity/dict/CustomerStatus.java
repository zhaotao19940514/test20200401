package cn.com.taiji.css.entity.dict;

public enum CustomerStatus {
	ENABLE("启用", 1) {},
	CANCELLATION("注销", 2) {},
	PRECANCELLATION("预注销", 3) {};
	private String value;
	private int code;

	private CustomerStatus(String value, int code) {
		this.value = value;
		this.code = code;
	}

	public String getValue() {
		return value;
	}

	public void setValue(String value) {
		this.value = value;
	}

	public int getCode() {
		return code;
	}

	public void setCode(int code) {
		this.code = code;
	}

}
