package cn.com.taiji.css.model.administration.inventory;

import cn.com.taiji.common.model.BaseModel;
import cn.com.taiji.qtk.entity.ServiceHall;

public class StorageOperationRequest extends BaseModel {
	//批次号
	private String batchId;
	//起始id；
	private String startId;
	//终止id
	private String endId;
	//卡品牌
	private String brand;
	//入库数量
	private Integer inboundNum;
	//出库数量
	private Integer outboundNum;
	//型号
	private String model;
	//类型
	private Integer type;
	//入库网点
	private ServiceHall inServiceHall;
	//出库网点
	private ServiceHall outServiceHall;
	private String userName;
	
	public String getBatchId() {
		return batchId;
	}
	public void setBatchId(String batchId) {
		this.batchId = batchId;
	}
	public String getStartId() {
		return startId;
	}
	public void setStartId(String startId) {
		this.startId = startId;
	}
	public String getEndId() {
		return endId;
	}
	public void setEndId(String endId) {
		this.endId = endId;
	}
	public String getBrand() {
		return brand;
	}
	public void setBrand(String brand) {
		this.brand = brand;
	}
	public Integer getInboundNum() {
		return inboundNum;
	}
	public void setInboundNum(Integer inboundNum) {
		this.inboundNum = inboundNum;
	}
	public Integer getOutboundNum() {
		return outboundNum;
	}
	public void setOutboundNum(Integer outboundNum) {
		this.outboundNum = outboundNum;
	}
	public String getModel() {
		return model;
	}
	public void setModel(String model) {
		this.model = model;
	}
	public Integer getType() {
		return type;
	}
	public void setType(Integer type) {
		this.type = type;
	}
	public ServiceHall getInServiceHall() {
		return inServiceHall;
	}
	public void setInServiceHall(ServiceHall inServiceHall) {
		this.inServiceHall = inServiceHall;
	}
	public ServiceHall getOutServiceHall() {
		return outServiceHall;
	}
	public void setOutServiceHall(ServiceHall outServiceHall) {
		this.outServiceHall = outServiceHall;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	
}
