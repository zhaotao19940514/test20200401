@XStreamAlias("response")
public class SampleCommResponse extends AbstractCssResponse
{
	private Calendar time;
	private String info;

	public Calendar getTime()
	{
		return time;
	}

	public String getInfo()
	{
		return info;
	}

	public SampleCommResponse setTime(Calendar time)
	{
		this.time = time;
		return this;
	}

	public SampleCommResponse setInfo(String info)
	{
		this.info = info;
		return this;
	}

}
